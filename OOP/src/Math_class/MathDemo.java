
package Math_class;


public class MathDemo {

    
    public static void main(String[] args) {
        //double x= Math.abs(-9.5);
        //System.out.println(x);
        
        System.out.println(Math.abs(-9.5));
        System.out.println(Math.sqrt(25.0));
        System.out.println(Math.pow(2, 3));
        System.out.println(Math.PI);
        System.out.println(Math.log(2.0));
        System.out.println(Math.exp(1.0));
        System.out.println(Math.max(-2.5, -12.6));
        System.out.println(Math.min(-2.5, -12.6));
        System.out.println(Math.ceil(9.5));
        System.out.println(Math.floor(9.5));
    
    }
    
}
